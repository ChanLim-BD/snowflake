-- 파일 형식 정의gz
CREATE OR REPLACE FILE FORMAT my_csvgz_format
TYPE = 'CSV'
COMPRESSION = 'GZIP'
FIELD_DELIMITER = '|'
PARSE_HEADER = TRUE
ERROR_ON_COLUMN_COUNT_MISMATCH = FALSE;

-- Parquet
CREATE OR REPLACE FILE FORMAT my_parquet_format
  NULL_IF = ('NULL', 'null')
  SNAPPY_COMPRESSION = TRUE
  TYPE = parquet;



-- INFER_SCHEMA test csv
SELECT *
