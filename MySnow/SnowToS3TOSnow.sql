-- FILE FORMAT 생성 (CSV)
CREATE OR REPLACE FILE FORMAT my_csv_unload_format
  TYPE = 'CSV'
  FIELD_DELIMITER = '|';

-- FILE FORMAT 생성 (Parquet)
CREATE OR REPLACE FILE FORMAT my_parquet_unload_format
  TYPE = PARQUET
  COMPRESSION = SNAPPY;

-- External Stage 생성 (CSV)
CREATE OR REPLACE STAGE SNOW_TO_S3_csv
    STORAGE_INTEGRATION = PENTA_INTEGRATION
    URL = 's3://jaeyeong-hyundai/snowflake_sample/chan/csv/'
    FILE_FORMAT = my_csv_unload_format;

-- External Stage 생성 (Parquet)
CREATE OR REPLACE STAGE SNOW_TO_S3_par
    STORAGE_INTEGRATION = PENTA_INTEGRATION
    URL = 's3://jaeyeong-hyundai/snowflake_sample/chan/parquet/'
    FILE_FORMAT = my_parquet_unload_format;


