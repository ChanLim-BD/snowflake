    TYPE = 'CSV'
    COMPRESSION = 'GZIP'
    FIELD_DELIMITER = '|'
    PARSE_HEADER = TRUE;


-----------------------------------------------
--                                           --
--         S3мҷҖ м—°кІ°мқ„ мң„н•ң  STAGE мғқм„ұ        --
