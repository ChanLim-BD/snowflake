-----------------------------------------------

  
-- External Stage мғқм„ұ (CSV)
CREATE OR REPLACE STAGE S3_TO_SNOW_csv
    STORAGE_INTEGRATION = PENTA_INTEGRATION
    URL = 's3://jaeyeong-hyundai/snowflake_sample/chan/csv/'
    FILE_FORMAT = my_csv_load_format;


----------------------------------------------------
--                                                --
--   INFER_SCHEMAлҘј нҶөн•ҙ S3мқҳ нҢҢмқј л©”нғҖлҚ°мқҙн„° мқёмӢқ    --
--                                                --
----------------------------------------------------

-- INFER_SCHEMA test csv
SELECT *
  FROM TABLE(
    INFER_SCHEMA(
      LOCATION=>'@S3_TO_SNOW_csv/'
      , FILE_FORMAT=>'my_csv_load_format'
      )
    );

-- Table мғқм„ұ м „м—җ ARRAY_AGG(object_construct(*)) кіјм • нҷ•мқёмҡ©
SELECT ARRAY_AGG(object_construct(*))
  FROM TABLE(
    INFER_SCHEMA(
      LOCATION=>'@S3_TO_SNOW_csv/'
      , FILE_FORMAT=>'my_csv_load_format'
      )
    );


------------------------------------------------
--                                            --
--    SCHEMA DETECTIONмқ„ нҷңмҡ©н•ң Table мғқм„ұ      --
--                                            --
------------------------------------------------



-- CSV Table мғқм„ұ
CREATE OR REPLACE TABLE S3TOSNOWCSV --ENABLE_SCHEMA_EVOLUTION = TRUE
USING TEMPLATE (
    SELECT ARRAY_AGG(object_construct(*))
      FROM TABLE(
        INFER_SCHEMA(
          LOCATION=>'@S3_TO_SNOW_csv/'
          , FILE_FORMAT=>'my_csv_load_format'
          )
      ));



-- Table мғқм„ұ нҷ•мқё      
SELECT * FROM S3TOSNOWCSV;

------------------------------------------------
--                                            --
--     Tableм—җ S3м—җ м Ғмһ¬лҗң лҚ°мқҙн„° COPY INTO     --
--                                            --
------------------------------------------------


COPY INTO S3TOSNOWCSV
  FROM @S3_TO_SNOW_csv
  FILE_FORMAT = my_csv_load_format
  MATCH_BY_COLUMN_NAME = CASE_INSENSITIVE;

-- Table лҚ°мқҙн„° м Ғмһ¬ нҷ•мқё      
SELECT * FROM S3TOSNOWCSV;


